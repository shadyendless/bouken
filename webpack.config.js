const HtmlWebPackPlugin = require('html-webpack-plugin');

module.exports = {
    entry: './src/js',
    output: {
        path: __dirname + '/assets',
        filename: 'js/app.js'
    },
    module: {
        rules: [
            {
                test: /\.(js|jsx)$/,
                exclude: /node_modules/,
                use: {
                    loader: 'babel-loader'
                }
            },
            {
                test: /\.html$/,
                use: [
                    {
                        loader: 'html-loader',
                        options: { minimize: true }
                    }
                ]
            }
        ]
    },
    plugins: [
        new HtmlWebPackPlugin({
            template: './src/index.html',
            filename: './index.html',
            hash: true
        })
    ]
};